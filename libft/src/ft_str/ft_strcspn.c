#include "libft.h"

size_t	ft_strcspn(const char *s1, const char *s2)
{
	const char	*s = s1;
	const char	*c;

	while (*s1)
	{
		c = s2;
		while (*c)
		{
			if (*s1 == *c)
				break ;
			c++;
		}
		if (*c)
			break ;
		s1++;
	}
	return (s1 - s);
}
